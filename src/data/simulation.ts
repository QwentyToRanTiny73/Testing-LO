// Данные интерактивного тренажёра «От лозы до бутылки».
// Каждый сценарий — последовательность этапов с решениями; выбор меняет
// пять метрик вина и может включать «дефект» (fault), влияющий на итог.
// Все числовые значения в текстах — ориентиры, а не нормативы.

export type MetricKey = 'aroma' | 'freshness' | 'structure' | 'stability' | 'purity';

export interface MetricDef {
  key: MetricKey;
  label: string;
  short: string;
  icon: string;
}

