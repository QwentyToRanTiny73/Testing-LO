// Данные интерактивного тренажёра «От лозы до бутылки».
// Каждый сценарий — последовательность этапов с решениями; выбор меняет
// пять метрик вина и может включать «дефект» (fault), влияющий на итог.
// Все числовые значения в текстах — ориентиры, а не нормативы.

export type MetricKey = 'aroma' | 'freshness' | 'structure' | 'stability' | 'purity';

export interface MetricDef {
  key: MetricKey;
  label: string;
  short: string;
  icon: string;
}

export const METRICS: MetricDef[] = [
  { key: 'aroma', label: 'Ароматический потенциал', short: 'Аромат', icon: '🌸' },
  { key: 'freshness', label: 'Свежесть и баланс', short: 'Свежесть', icon: '🍋' },
  { key: 'structure', label: 'Структура и тело', short: 'Структура', icon: '🏛' },
  { key: 'stability', label: 'Стабильность', short: 'Стабильность', icon: '🛡' },
  { key: 'purity', label: 'Чистота (без дефектов)', short: 'Чистота', icon: '✨' },
];

export const INITIAL_METRICS: Record<MetricKey, number> = {
  aroma: 55,
  freshness: 55,
  structure: 55,
  stability: 60,
  purity: 70,
};

export interface SimEffect {
  metric: MetricKey;
  delta: number;
}

export type ChoiceQuality = 'best' | 'ok' | 'poor';

/** Стиль вина — определяет формулировки дегустационной заметки в итоге. */
export type NoteStyle = 'white' | 'red' | 'rose' | 'orange';

export interface SimChoice {
  id: string;
  label: string;
  detail?: string;
  effects: SimEffect[];
  /** Объяснение последствий (показывается после выбора). */
  feedback: string;
  quality: ChoiceQuality;
  /** Имя дефекта, если выбор его провоцирует (для итогового разбора). */
  fault?: string;
}

export interface SimStage {
  id: string;
  phase: string;
  icon: string;
  title: string;
  context: string;
  /** Панель «анализа» — показатели партии на этом этапе. */
  data?: { label: string; value: string }[];
  question: string;
  choices: SimChoice[];
}

export interface SimScenario {
  id: string;
  title: string;
  grape: string;
  style: string;
  icon: string;
  difficulty: string;
  noteStyle: NoteStyle;
  intro: string;
  goal: string;
  stages: SimStage[];
}

